    static {
        boolean hasSignTypes = true;

        try {
            org.bukkit.Material.valueOf("OAK_SIGN");
        } catch (Exception e) {
            hasSignTypes = false;
        }

        if (!hasSignTypes) {
            String[] signNames = {"ACACIA_SIGN", "BIRCH_SIGN", "DARK_OAK_SIGN", "JUNGLE_SIGN", "OAK_SIGN", "SPRUCE_SIGN",};
            String[] wallSignNames = {"ACACIA_WALL_SIGN", "BIRCH_WALL_SIGN", "DARK_OAK_WALL_SIGN", "JUNGLE_WALL_SIGN", "OAK_WALL_SIGN", "SPRUCE_WALL_SIGN",};

            org.bukkit.Material signMaterial = org.bukkit.Material.valueOf("SIGN");
            org.bukkit.Material wallSignMaterial = org.bukkit.Material.valueOf("SIGN");

            try {
                for (String signName : signNames) {
                    toBukkit.put(Material.valueOf(signName), signMaterial);
                }

                for (String wallSignName : wallSignNames) {
                    toBukkit.put(Material.valueOf(wallSignName), wallSignMaterial);
                }
            } catch (Exception e) {
                //
            }

            toAtla.put(signMaterial, Material.OAK_SIGN);
            toAtla.put(wallSignMaterial, Material.OAK_WALL_SIGN);
        }

        if (Bukkit.getVersion().contains(" 1.13")) {
            map1_13();
        }
    }

    public static Material convert(org.bukkit.Material bukkitMaterial) {
        Material result = toAtla.get(bukkitMaterial);

        if (result != null) {
            return result;
        }

        return Material.values()[bukkitMaterial.ordinal()];
    }

    public static org.bukkit.Material convert(Material material) {
        org.bukkit.Material result = toBukkit.get(material);

        if (result != null) {
            return result;
        }

        return org.bukkit.Material.values()[material.ordinal()];
    }

